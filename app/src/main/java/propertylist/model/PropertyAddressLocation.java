package propertylist.model;

public class PropertyAddressLocation implements java.io.Serializable {

    private float lon;
    private float lat;

    public float getLon() {
        return this.lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }

    public float getLat() {
        return this.lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }
}
